Inloggegevens
-------------

U kunt als volgt inloggen:

1. Ga naar https://www.sportschoolgeelhoed.nl
2. Klik op het slotje in de rechterbovenhoek
3. Log in met de volgende gegevens:
   - E-mailadres: {!! $email !!}
   - Wachtwoord: {!! $password !!}

Nu u bent ingelogd, kunt u uw kinderen (of uzelf, als u wedstrijdjudoka bent) inschrijven voor wedstrijden en deze inschrijvingen beheren.

Alle opties die u heeft kunt u vinden in uw gebruikersmenu, dat u in de rechterbovenhoek kunt vinden (aangeven door een pictogram van een hoofd).

U heeft hier twee opties:
- Wedstrijdagenda: een overzicht van wedstrijden waarvoor u kunt inschrijven
- Mijn wedstrijden: een overzicht van alle wedstijden waarvoor u iemand heeft ingeschreven. Hier kunt u ook de status van uw inschrijvingen bekijken en openstaande betalingen verrichten.

U kunt als volgt inschrijven:
1. Ga naar de Wedstrijdagenda
2. Klik op "meer informatie en inschrijven"
3. Scroll naar beneden en klik op "Inschrijven" naast de judoka die u wilt inschrijven.
4. Vul het formulier in en klik op Inschrijven.
5. Er wordt u gevraagd om te betalen. Nadat u deze betaling heeft verricht is de inschrijving definitief.

