Van harte welkom,

Wat leuk dat jullie gaan meedoen met onze wedstrijdgroep.

Met jullie bedoelen we zowel judoka, als ouder(s) en/of verzorger(s). Dat zeggen we expres zo. Een judoka die geen steun krijgt van thuis, kan wel graag willen, maar komt niet veel verder in dit mooie spelletje. We staan te popelen om samen met onze trainers, vrijwilligers en natuurlijk de ouders het talent van de judoka’s zo optimaal mogelijk te ontwikkelen. Hiervoor hebben we alle selectiegroep judoka’s nodig. Ieder van deze judoka, groot of klein heeft zijn of haar eigen sterke punten. Wat alle judoka’s gezamenlijk hebben, is de wil om hard te werken en niet op te geven. Als wedstrijdjudoka en misschien wel topjudoka worden de droom is, zal dat dus altijd een teamprestatie zijn! Welkom bij het team van sportschool Geelhoed!

Jullie ontvangen in deze mail verder een gebruikersnaam en wachtwoord voor onze site. Dit geeft toegang tot dat deel van de site waarin men zich kan opgeven voor de wedstrijden. Ook een uitleg hoe het opgeven voor evenementen in zijn werk gaat staat in deze mail. Tot slot ontvangen jullie ook nog een handleiding van wat jullie zoal te wachten staat als er voor het eerst wordt meegedaan aan een officiële judowedstrijd. Dit alles om goed voorbereid in de auto te stappen op weg naar dat eerste toernooi. Spannend natuurlijk, maar bedenk altijd, ook grote kampioenen hebben ooit deze vierde stap gezet.

Vierde stap? Ja, de eerste stap was op judo gaan. De tweede stap is meedoen aan en opgeven voor onze wedstrijdploeg, de derde stap is het daadwerkelijk opgeven voor een wedstrijd, en de vierde stap is dus het echte werk, meedoen aan een officieel toernooi!

Voor wie het allemaal een beetje (te) spannend is: Spanning hoort er bij, dat geeft aan dat het belangrijk voor je is. De kunst is niet om geen spanning te hebben, de kunst is, hoe ga ik met die spanning om. Bedenk dan dat je misschien ook spanning voelde bij de eerste twee stappen, en dat is ook goed gekomen. Je mag dus op jezelf vertrouwen, dat ook dit vanzelf wel goed komt!

Veel succes!

Tonny Mulder

